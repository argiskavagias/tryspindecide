// --- URL ROUTING ---
function encodeWheelState() {
    const state = {
        m: currentMode,
        t: elements.sidebarTitle.textContent
    };
    
    if (currentMode === 'custom' || currentMode === 'dinner' || currentMode === 'yesno') {
        state.i = elements.itemInput.value;
    } else if (currentMode === 'team') {
        state.p = elements.playerInput.value;
        state.c = elements.teamCount.value;
    } else if (currentMode === 'dare') {
        state.d = elements.dareDifficulty.value;
        state.uc = elements.customDareCheck.checked;
        state.cd = elements.customDareInput.value;
    } else if (currentMode === 'santa') {
        state.s = elements.santaNames.value;
    }
    
    const jsonStr = JSON.stringify(state);
    const hash = UTF8Base64.encode(jsonStr);
    
    return `${window.location.origin}${window.location.pathname}#wState=${hash}`;
}

function decodeWheelState() {
    const hash = window.location.hash;
    if (!hash) return;
    
    try {
        if (hash.includes('santaGiver=') && hash.includes('giftRecipient=')) {
            const params = new URLSearchParams(hash.substring(1));
            const giver = decodeURIComponent(params.get('santaGiver'));
            const encryptedGift = params.get('giftRecipient');
            const recipient = UTF8Base64.decode(encryptedGift);
            
            const isEn = currentLang === 'en';
            
            elements.resultValue.innerHTML = isEn 
                ? `🎄 Hello <span style="color:var(--accent-cyan)">${giver}</span>!<br><br>You are the Secret Santa of:<br><span style="color:var(--accent-purple)">${recipient}</span>! 🎁`
                : `🎄 Γεια σου <span style="color:var(--accent-cyan)">${giver}</span>!<br><br>Είσαι ο Secret Santa του/της:<br><span style="color:var(--accent-purple)">${recipient}</span>! 🎁`;
                
            elements.resultTitle.textContent = isEn ? '🎄 Secret Santa Reveal! 🎄' : '🎄 Secret Santa Αποκάλυψη! 🎄';
            elements.resultOverlay.classList.add('active');
            
            startConfetti();
            playVictorySound();
            return;
        }
        
        if (hash.startsWith('#wState=')) {
            const encodedState = hash.split('#wState=')[1];
            const jsonStr = UTF8Base64.decode(encodedState);
            const state = JSON.parse(jsonStr);
            
            if (state.m) {
                currentMode = state.m;
                selectMode(state.m);
                
                if (state.m === 'custom' || state.m === 'dinner' || state.m === 'yesno') {
                    elements.itemInput.value = state.i || '';
                } else if (state.m === 'team') {
                    elements.playerInput.value = state.p || '';
                    elements.teamCount.value = state.c || 2;
                } else if (state.m === 'dare') {
                    elements.dareDifficulty.value = state.d || 'party';
                    elements.customDareCheck.checked = state.uc || false;
                    elements.customDareInput.value = state.cd || '';
                    if (state.uc) {
                        elements.customDareInput.classList.remove('hidden');
                    }
                } else if (state.m === 'santa') {
                    elements.santaNames.value = state.s || '';
                }
                
                updateWheelFromInputs();
            }
        }
    } catch (e) {
        console.error('Failed to parse URL hash state:', e);
    }
}

