// --- SECRET SANTA ---
function performSecretSanta() {
    const text = elements.santaNames.value.trim();
    const names = text ? text.split('\n').filter(x => x.trim()) : [];
    
    if (names.length < 2) {
        alert(currentLang === 'en' ? 'Enter at least 2 names for Secret Santa!' : 'Εισάγετε τουλάχιστον 2 ονόματα για την κλήρωση Secret Santa!');
        return;
    }
    
    let giver = [...names];
    let receiver = [...names];
    let matched = false;
    let attempts = 0;
    
    while (!matched && attempts < 100) {
        attempts++;
        matched = true;
        receiver.sort(() => Math.random() - 0.5);
        
        for (let i = 0; i < giver.length; i++) {
            if (giver[i] === receiver[i]) {
                matched = false;
                break;
            }
        }
    }
    
    if (!matched) {
        alert(currentLang === 'en' ? 'Match failed. Try again!' : 'Η κλήρωση απέτυχε. Δοκιμάστε ξανά!');
        return;
    }
    
    const baseDomain = window.location.origin + window.location.pathname;
    elements.santaLinksList.innerHTML = '';
    
    giver.forEach((person, idx) => {
        const giftName = receiver[idx];
        const encryptedGift = UTF8Base64.encode(giftName);
        const encodedGiver = encodeURIComponent(person);
        const santaLink = `${baseDomain}#santaGiver=${encodedGiver}&giftRecipient=${encryptedGift}`;
        
        const row = document.createElement('div');
        row.className = 'santa-link-row';
        row.innerHTML = `
            <span class="santa-link-name">${person}</span>
            <button class="copy-row-btn" data-url="${santaLink}">${currentLang === 'en' ? 'Copy Link' : 'Αντιγραφή Link'}</button>
        `;
        
        elements.santaLinksList.appendChild(row);
    });
    
    elements.santaOverlay.classList.add('active');
}

