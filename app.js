function startConfetti() {
    const container = document.getElementById('confettiCanvasContainer');
    container.innerHTML = '';
    
    confettiCanvas = document.createElement('canvas');
    confettiCanvas.style.width = '100%';
    confettiCanvas.style.height = '100%';
    confettiCanvas.style.position = 'absolute';
    confettiCanvas.style.top = '0';
    confettiCanvas.style.left = '0';
    container.appendChild(confettiCanvas);
    
    confettiCanvas.width = container.clientWidth;
    confettiCanvas.height = container.clientHeight;
    confettiCtx = confettiCanvas.getContext('2d');
    
    fireworkRockets = [];
    fireworkParticles = [];
    
    // Spawn 3 rockets climbing up
    const centerX = confettiCanvas.width / 2;
    const centerY = confettiCanvas.height / 3;
    
    fireworkRockets.push(new FireworkRocket(centerX, centerY));
    fireworkRockets.push(new FireworkRocket(centerX - 100, centerY + 30));
    fireworkRockets.push(new FireworkRocket(centerX + 100, centerY + 30));
    
    if (confettiAnimationId) cancelAnimationFrame(confettiAnimationId);
    animateFireworks();
}

function spawnFireworkSparks(x, y) {
    // Spawn 40+ sparks on explosion
    for (let i = 0; i < 45; i++) {
        fireworkParticles.push(new FireworkParticle(x, y));
    }
}

function animateFireworks() {
    if (!confettiCtx) return;
    confettiCtx.clearRect(0, 0, confettiCanvas.width, confettiCanvas.height);
    
    // 1. Update Rockets
    fireworkRockets = fireworkRockets.filter(r => !r.exploded);
    fireworkRockets.forEach(r => {
        r.update();
        r.draw(confettiCtx);
    });
    
    // 2. Update Sparks
    fireworkParticles = fireworkParticles.filter(p => p.opacity > 0);
    fireworkParticles.forEach(p => {
        p.update();
        p.draw(confettiCtx);
    });
    
    // Keep animating if rockets or particles exist
    if (fireworkRockets.length > 0 || fireworkParticles.length > 0) {
        confettiAnimationId = requestAnimationFrame(animateFireworks);
    }
}

// --- CANVAS WHEEL RENDERING (THEME AWARE) ---
function drawWheel() {
    const size = 600;
    const center = size / 2;
    const radius = center - 15;
    
    ctx.clearRect(0, 0, size, size);
    
    const count = wheelItems.length;
    if (count === 0) {
        ctx.save();
        ctx.translate(center, center);
        ctx.beginPath();
        ctx.arc(0, 0, radius, 0, Math.PI * 2);
        ctx.fillStyle = currentTheme === 'light' ? '#e5e7eb' : '#111827';
        ctx.fill();
        ctx.lineWidth = 4;
        ctx.strokeStyle = currentTheme === 'light' ? '#d1d5db' : '#374151';
        ctx.stroke();
        ctx.restore();
        return;
    }
    
    const arcSize = (Math.PI * 2) / count;
    const activeColors = THEME_COLORS[currentTheme] || THEME_COLORS.cyber;
    
    for (let i = 0; i < count; i++) {
        const angle = startAngle + i * arcSize;
        ctx.save();
        ctx.translate(center, center);
        ctx.rotate(angle);
        
        ctx.beginPath();
        ctx.moveTo(0, 0);
        ctx.arc(0, 0, radius, 0, arcSize);
        ctx.closePath();
        
        const colorIndex = i % activeColors.slices.length;
        const color = activeColors.slices[colorIndex];
        
        const grad = ctx.createRadialGradient(0, 0, 10, 0, 0, radius);
        grad.addColorStop(0, color);
        grad.addColorStop(1, adjustColorBrightness(color, -25));
        
        ctx.fillStyle = grad;
        ctx.fill();
        
        ctx.lineWidth = 1.5;
        ctx.strokeStyle = currentTheme === 'light' ? 'rgba(0,0,0,0.05)' : 'rgba(255,255,255,0.06)';
        ctx.stroke();
        
        ctx.rotate(arcSize / 2);
        ctx.textAlign = 'right';
        ctx.textBaseline = 'middle';
        
        let fontSize = Math.max(14, Math.min(24, 300 / count));
        ctx.font = `600 ${fontSize}px 'Outfit', sans-serif`;
        ctx.fillStyle = currentTheme === 'light' && colorIndex % 2 !== 0 ? '#111827' : '#ffffff';
        
        let text = wheelItems[i];
        if (text.length > 18) text = text.substring(0, 16) + '...';
        
        ctx.fillText(text, radius - 30, 0);
        ctx.restore();
    }
    
    ctx.save();
    ctx.translate(center, center);
    ctx.beginPath();
    ctx.arc(0, 0, radius, 0, Math.PI * 2);
    ctx.lineWidth = 8;
    ctx.strokeStyle = activeColors.border;
    ctx.stroke();
    
    ctx.beginPath();
    ctx.arc(0, 0, radius - 4, 0, Math.PI * 2);
    ctx.lineWidth = 2;
    ctx.strokeStyle = activeColors.borderInner;
    ctx.stroke();
    ctx.restore();
}

function adjustColorBrightness(hex, percent) {
    let R = parseInt(hex.substring(1, 3), 16);
    let G = parseInt(hex.substring(3, 5), 16);
    let B = parseInt(hex.substring(5, 7), 16);

    R = parseInt((R * (100 + percent)) / 100);
    G = parseInt((G * (100 + percent)) / 100);
    B = parseInt((B * (100 + percent)) / 100);

    R = R < 255 ? R : 255;
    G = G < 255 ? G : 255;
    B = B < 255 ? B : 255;

    R = R > 0 ? R : 0;
    G = G > 0 ? G : 0;
    B = B > 0 ? B : 0;

    const rHex = R.toString(16).padStart(2, '0');
    const gHex = G.toString(16).padStart(2, '0');
    const bHex = B.toString(16).padStart(2, '0');

    return `#${rHex}${gHex}${bHex}`;
}

// --- PHYSICS ENGINE FOR SPINNING ---
function spinWheel() {
    if (isSpinning) return;
    initAudio();
    
    isSpinning = true;
    spinVelocity = Math.random() * 0.4 + 0.35; 
    lastTickAngle = startAngle;
    
    requestAnimationFrame(updateSpin);
}

function updateSpin() {
    if (spinVelocity > 0.001) {
        startAngle += spinVelocity;
        startAngle = startAngle % (Math.PI * 2);
        spinVelocity *= friction;
        
        const count = wheelItems.length;
        if (count > 0) {
            const arcSize = (Math.PI * 2) / count;
            const currentSegment = Math.floor(((Math.PI * 2) - startAngle) / arcSize) % count;
            const lastSegment = Math.floor(((Math.PI * 2) - lastTickAngle) / arcSize) % count;
            
            if (currentSegment !== lastSegment) {
                playTickSound();
            }
        }
        lastTickAngle = startAngle;
        
        drawWheel();
        requestAnimationFrame(updateSpin);
    } else {
        isSpinning = false;
        spinVelocity = 0;
        determineWinner();
    }
}

function determineWinner() {
    const count = wheelItems.length;
    if (count === 0) return;
    
    const arcSize = (Math.PI * 2) / count;
    const relativeAngle = (3 * Math.PI / 2 - startAngle + Math.PI * 2) % (Math.PI * 2);
    const winningIndex = Math.floor(relativeAngle / arcSize) % count;
    const winner = wheelItems[winningIndex];
    
    showResult(winner);
}

