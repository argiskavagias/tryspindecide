// --- ADVANCED FIREWORKS PARTICLE SYSTEM ---
class FireworkRocket {
    constructor(targetX, targetY) {
        this.x = targetX + (Math.random() * 140 - 70);
        this.y = confettiCanvas.height + 20;
        this.targetX = targetX;
        this.targetY = targetY;
        this.speed = 10;
        this.angle = Math.atan2(this.targetY - this.y, this.targetX - this.x);
        this.velocity = {
            x: Math.cos(this.angle) * this.speed,
            y: Math.sin(this.angle) * this.speed
        };
        this.exploded = false;
    }
    update() {
        this.x += this.velocity.x;
        this.y += this.velocity.y;
        
        const dist = Math.hypot(this.targetX - this.x, this.targetY - this.y);
        if (dist < 15 || this.velocity.y >= 0) {
            this.exploded = true;
            spawnFireworkSparks(this.x, this.y);
        }
    }
    draw(ctx) {
        ctx.save();
        ctx.beginPath();
        ctx.arc(this.x, this.y, 4, 0, Math.PI * 2);
        ctx.fillStyle = '#ffffff';
        ctx.shadowBlur = 12;
        ctx.shadowColor = '#06b6d4';
        ctx.fill();
        ctx.restore();
    }
}

class FireworkParticle {
    constructor(x, y) {
        this.x = x;
        this.y = y;
        this.radius = Math.random() * 3.5 + 1.2;
        this.color = `hsl(${Math.random() * 360}, 100%, 65%)`;
        this.angle = Math.random() * Math.PI * 2;
        this.speed = Math.random() * 6 + 1.5;
        this.gravity = 0.12;
        this.friction = 0.94;
        this.opacity = 1;
        this.fade = Math.random() * 0.012 + 0.008;
        this.velocity = {
            x: Math.cos(this.angle) * this.speed,
            y: Math.sin(this.angle) * this.speed
        };
    }
    update() {
        this.velocity.x *= this.friction;
        this.velocity.y *= this.friction;
        this.velocity.y += this.gravity;
        this.x += this.velocity.x;
        this.y += this.velocity.y;
        this.opacity -= this.fade;
    }
    draw(ctx) {
        ctx.save();
        ctx.globalAlpha = this.opacity;
        ctx.beginPath();
        ctx.arc(this.x, this.y, this.radius, 0, Math.PI * 2);
        ctx.fillStyle = this.color;
        ctx.shadowBlur = 10;
        ctx.shadowColor = this.color;
        ctx.fill();
        ctx.restore();
    }
}

